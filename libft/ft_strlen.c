#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	len;

	len = 0;
	while (*s)
	{
		len++;
		s++;
	}
	return (len);
}

size_t	safe_strlen(char *s)
{
	size_t	len = 0;
	if (!s)
		return (0);
	while (s[len])
		len++;
	return (len);
}


